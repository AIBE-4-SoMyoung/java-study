package day251022;

public class _01_HelloWorld {
    // 실행 -> main() 메서드가 필요
    // main
    public static void main(String[] args) {
        // 출력
        // python : print
        // javascript : console.log
        // c : printf

        // System -> out -> print (출력 -> 줄바꿈없이 출력)
        // println -> line -> 줄바꿈이 들어간 개념
        System.out.println("Hello World"); // 문자열. "". Java는 문자<->문자열
        // "" -> String의 특수한 방식의 생성자
        // '' -> char (character) 문자.
        // Eclipse - sysout -> 단축 기능
        System.out.println("Hello World"); // sout
    }
}
